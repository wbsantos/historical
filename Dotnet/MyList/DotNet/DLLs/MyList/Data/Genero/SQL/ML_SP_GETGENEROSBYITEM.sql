IF EXISTS (SELECT * FROM SYSOBJECTS WHERE TYPE = 'P' AND NAME = 'ML_SP_GETGENEROSBYITEM')
	BEGIN
		PRINT 'DROPPING PROCEDURE ML_SP_GETGENEROSBYITEM'
		DROP  PROCEDURE ML_SP_GETGENEROSBYITEM
	END
GO

PRINT 'CREATING PROCEDURE ML_SP_GETGENEROSBYITEM'
GO

CREATE PROCEDURE ML_SP_GETGENEROSBYITEM
	@ITEMID INT
AS

/******************************************************************************
**		NOME DO AQUIVO: ML_SP_GETGENEROSBYITEM.SQL
**		NOME DA PROCEDURE: ML_SP_GETGENEROSBYITEM
**
**		DESCRIÇÃO: PROCEDURE RESPONSÁVEL EM CONSULTAR GENEROS CADASTRADOS FILTRANDO POR CATEGORIA.
**      
**
**		TEAMPLATE ELABORADO POR: WILLIAM BARBOSA
**              
**		PROGRAMADOR AUTOR: WILLIAM BARBOSA
**		DATA: 03/11/2015
*******************************************************************************
**		HISTÓRICO DE ALTERAÇÕES
*******************************************************************************
**		DATA:		AUTOR:				DESCRIÇÃO:
**
*******************************************************************************/

SELECT GRID, GRGENERO FROM GENERO WITH(NOLOCK)
INNER JOIN ITEM_GENERO WITH(NOLOCK) ON IGGRID = GRID
WHERE IGITID = @ITEMID

RETURN 0
